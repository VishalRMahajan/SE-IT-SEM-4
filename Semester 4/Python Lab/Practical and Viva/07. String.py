string = "vishal MAHAJAN"

# Convert all characters in the string to uppercase
print("Uppercase string: ", string.upper())

# Convert all characters in the string to lowercase
print("Lowercase string: ", string.lower())

# Capitalize the first character of the string
print("Capitalized string: ", string.capitalize())

# Split the string into a list of words
print("Split string: ", string.split(" "))

# Replace all occurrences of 'v' with 'V' in the string
print("Replaced 'v' with 'V': ", string.replace("v","V"))

# Check if all characters in the string are uppercase
print("Is all uppercase? ", string.isupper())

# Check if all characters in the string are lowercase
print("Is all lowercase? ", string.islower())

# Check if the string is a decimal
string1 ="10"
print("Is decimal? ", string1.isdecimal())

# Count the number of occurrences of 'A' in the string
print("Count of 'A': ", string.count("A"))

# Find the index of the first occurrence of 'v' in the string
print("Index of 'v': ", string.index("v"))